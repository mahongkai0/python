def xianshi(L):
    print("+-------------+-------------+-------------+")
    print("|     姓名    |    年龄     |  成绩       |")
    print("+-------------+-------------+-------------+")
    for x in range(len(L)):
        print(
            "|" + L[x]['name'].center(13) + "|" + str(L[x]['age']).center(13) + "|" + str(L[x]['score']).center(13) + '|')
    print("+-------------+-------------+-------------+")
